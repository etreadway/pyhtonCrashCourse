# Function for building a list (in this case; sandwiches)
def sandwichMaker(*toppings):
    '''Builds a list from the inputs passed to the function.'''
    print('\nYou ordered a sandwich with:')
    for topping in toppings:
        print(topping)

sandwichMaker('bacon', 'lettuce', 'tomato')

sandwichMaker('peanut butter', 'banana', 'nutella')

